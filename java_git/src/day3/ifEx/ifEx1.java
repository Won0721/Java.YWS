package day3.ifEx;

public class ifEx1 {
	public static void main(String[] args) {
		// 정수 num이 0이면 0입니다 라고 출력하는 코드
		// 정수 num가 0이다
		// 정수 num가 0과 같다
		int num = 10;
		if(num==0) {
			//0입니다 출력;
			System.out.println("0입니다.");
		}
		// 정수 num가 0이 아니면 0이 아닙니다 라고 출력하는 코드를 작성하세요.
		if(num != 0) {
			System.out.println(num + "은 0이 아닙니다.");
		}
		
		
	}

}
