package day5.practice;

public class MultiplicationTableEx {
	public static void main(String[] args) {
		int i = 1 ;
		while(i < 10) {
			System.out.println("2 * "+ i + " = " + 2*i );
			i++;
		}
		System.out.println("============");
		for (int j=1; j<10; j++) {
			System.out.println("2 * "+ j + " = " + 2*j );

		}
		
	}

}
